#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	size_t			i;
	unsigned char	*temp;

	temp = (unsigned char *)s;
	i = 0;
	if (!s)
		return (NULL);
	while (i < n)
		temp[i++] = (unsigned char)c;
	return (temp);
}
