#include <unistd.h>

void	ft_putnbr(int nb)
{
	int	i;

	if (nb < 0)
	{	
		nb *= -1;
		write(1, "-", 1);
	}
	if (nb == -2147483648)
	{
		write(1, "2147483648", 10);
		return ;
	}
	if (nb > 9)
	{
		ft_putnbr (nb / 10);
	}
	i = nb % 10 + 48;
	write(1, &i, 1);
}
