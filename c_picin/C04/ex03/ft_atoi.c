int	ft_atoi(char *str)
{
	int	temp;
	int	i;
	int	flag;

	i = 0;
	temp = 0;
	flag = 0;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == ' ')
		i++;
	while (str[i] == '+' || str[i] == '-')
	{
		if (str[i] == '-')
			flag++;
		i++;
	}
	while (str[i] != '\0' && str[i] >= '0' && str[i] <= '9')
	{
		temp = (temp * 10) + (str[i] - 48);
		i++;
	}
	if (flag % 2 != 0)
		temp *= -1;
	return (temp);
}
