#include <unistd.h>

void	ft_putnbr(int nb)
{
	int	a;

	if (nb < 0)
	{
		nb = nb * -1;
		write(1, "-", 1);
	}	
	if (nb == -2147483648)
	{
		write(1, "2147483648", 10);
		return ;
	}
	if (nb > 9)
	{
		ft_putnbr (nb / 10);
	}
	a = nb % 10 + 48;
	write(1, &a, 1);
}
