#include <unistd.h>

void	number2(char *a, char *b)
{
	while (b[1] <= '9')
	{
		write(1, a, 2);
		write(1, " ", 1);
		write(1, b, 2);
		if (a[0] == '9' && a[1] == '8' && b[0] == '9' && b[1] == '9')
			break ;
		write(1, ",", 1);
		write(1, " ", 1);
		b[1]++;
	}
	b[0]++;
	b[1] = '0';
}

void	ft_print_comb2(void)
{
	char	a[2];
	char	b[2];

	a[0] = '0';
	a[1] = '0';
	b[1] = '1';
	while (a[0] <= '9')
	{
		while (a[1] <= '9')
		{
			b[0] = a[0];
			while (b[0] <= '9')
			{
				number2(a, b);
			}
			a[1]++;
			b[0] = '0';
			b[1] = a[1] + 1;
		}
		a[1] = '0';
		a[0]++;
		b[0] = a[0];
		b[1] = a[1] + 1;
	}
}
