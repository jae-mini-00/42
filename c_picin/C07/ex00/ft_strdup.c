#include <stdlib.h>

char	*ft_strdup(char *src)
{
	int		i;
	char	*c;

	i = 0;
	while (src[i] != '\0')
		i++;
	c = (char *)malloc (sizeof(char) * i + 1);
	if (c == 0)
		return (0);
	i = 0;
	while (src[i])
	{
		c[i] = src[i];
		i++;
	}
	c[i] = '\0';
	return (c);
}
