char	*ft_strstr(char *str, char *to_find)
{
	int	i;
	int	f_i;

	i = 0;
	if (*to_find == 0)
		return (str);
	while (str[i] != '\0')
	{
		f_i = 0;
		while (str[i + f_i] == to_find[f_i])
		{
			f_i++;
			if (to_find[f_i] == '\0')
				return (&str[i]);
		}
		i++;
	}
	return (0);
}
