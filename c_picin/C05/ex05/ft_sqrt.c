int	ft_sqrt(int nb)
{
	int	i;

	i = 1;
	if (nb == 1)
		return (1);
	if (nb < 0)
		return (0);
	while (nb > i)
	{
		if (nb == i * i)
			return (i);
		i++;
	}
	return (0);
}
