#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	int	a;

	if (fd <= 0)
		return ;
	if (n < 0)
	{
		write(fd, "-", 1);
		n *= -1;
	}
	if (n == -2147483648)
	{
		write(fd, "2147483648", 10);
		return ;
	}
	if (n > 9)
	{
		ft_putnbr_fd(n / 10, fd);
	}
	a = n % 10 + 48;
	write(fd, &a, 1);
}
